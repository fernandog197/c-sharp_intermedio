namespace Operaciones
{
    public class COperaciones
    {
        public static double Suma (double pA, double pB)
        {
            return pA + pB;
        }

        public static double Resta (double pA, double pB)
        {
            return pA - pB;
        }

        public static double Multiplicacion (double pA, double pB)
        {
            return pA * pB;
        }

        public static double Division (double pA, double pB)
        {
            return pA / pB;
        }
    }
}